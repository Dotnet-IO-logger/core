namespace Diol.Share.Utils
{
    /// <summary>
    /// Represents information about a dotnet process.
    /// </summary>
    public class DotnetProcessInfo
    {
        /// <summary>
        /// Gets or sets the ID of the dotnet process.
        /// </summary>
        public int Id { get; set; }

        /// <summary>
        /// Gets or sets the name of the dotnet process.
        /// </summary>
        public string Name { get; set; }
    }
}
